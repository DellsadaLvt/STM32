/*===========================================================================
*								LIBRARY
============================================================================*/
#include<stdio.h>
#include<stdint.h>

/*===========================================================================
*								DEFINE
============================================================================*/
#define add		0x01
#define sub		(0x01 <<1)
#define mul 	(0x01 <<2)
#define div		(0x01 <<3)

/*===========================================================================
*								FUNCTION
============================================================================*/
void caculate( uint16_t a, uint16_t b, unsigned char mod );

/*===========================================================================
*								MAIN
============================================================================*/
int main( void )
{
	caculate( 5, 4, (add | div));
	return 0;
}

/*===========================================================================
*								SUBROUTINE
============================================================================*/
void caculate( uint16_t a, uint16_t b, unsigned char mod )
{
	if( mod & add )
		printf("add result: %d \n", (a+b));
	if( mod & sub)
		printf("sub result: %d \n", (a - b));
	if( mod & mul)
		printf("mul result: %d \n", ( a * b));
	if( mod & div)
		printf("div result: %f \n", ((float)a /b));
}