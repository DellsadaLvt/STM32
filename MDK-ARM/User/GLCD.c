static user_func_status_t glcd_command(const uint8_t data){
	user_transmit(COMMAND_KEY);
	user_transmit((uint8_t)(data&0xF0));
	user_transmit((uint8_t)(data<<4u));
	
	return oke;
}

static user_func_status_t glcd_data(const uint8_t data){
	user_transmit(DATA_KEY);
	user_transmit((uint8_t)(data&0xF0));
	user_transmit((uint8_t)(data<<4u));

	return oke;
}

static user_func_status_t glcd_func_set(void){
	glcd_command(BASIC_INSTRUCTION);
	user_delay(1);
	glcd_command(BASIC_INSTRUCTION);
	user_delay(1);
	
	return oke;
}

static user_func_status_t glcd_display_control(void){
	glcd_command(DISPLAY_CONTROL);
	user_delay(1);
	
	return oke;
}

static user_func_status_t glcd_end_init(void){
	/* entry mode set */
	glcd_command(ENTRY_MODE_SET);
	
	/* Cursor display shift */
	//glcd_command(0x10);
	
	return oke;
}


static user_func_status_t glcd_user_CGRAM_init(void){
	glcd_command(EXTERNAL_INSTRUCTION);
	user_delay(1u);
	glcd_command(ENABLE_CGRAM_ADD);
	user_delay(1u);
	glcd_command(BASIC_INSTRUCTION);
	user_delay(1u);
	
	return oke;
}


user_func_status_t glcd_init( void ){
	user_delay(100u);
	
	glcd_func_set();
	glcd_display_clear();
	glcd_display_control();
	glcd_end_init();
	
	return oke;
}



